#include <stdio.h>

int main() {
    int i;
    for (i = 2; i <= 10; i += 2) {  // เริ่มจาก 2 จนถึง 10 ก้าวทีละ 2
        printf("%d ", i);
    }
    printf("\nEnd of loop\n");
    return 0;
}


// while loop ต้องกำหนดค่าเริ่มต้น (i = 2) และอัปเดตตัวแปรเอง (i += 2)

// for loopรวมทุกอย่าง (ค่าเริ่มต้น, เงื่อนไข, การเพิ่มค่า) ไว้ในบรรทัดเดียว ทำให้โค้ดสั้นและอ่านง่าย